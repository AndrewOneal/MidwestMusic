const api={
	endpoint:"http://localhost:3000/post",
	GET:function(callback){
		axios.get(`${api.endpoint}`,{}).then(function(response){
			callback(response);
		}).catch(function(error){
			console.log(error);
		});
	},
	PUT:function(data,callback){
		axios.put(`${api.endpoint}`,data).then(function(response){
			callback(response);
		}).catch(function(error){
			console.log(error);
		});
	},
	POST:function(data,callback){
		axios.post(`${api.endpoint}`,data).then(function(response){
			callback(response);
		}).catch(function(error){
			console.log(error);
		});
	},
	REMOVE:function(data,callback){
		api.GET(function(response){
			axios.delete(`${api.endpoint}/${response.data[data]._id}`,data).then(function(response){
				callback(response);
			}).catch(function(error){
				console.log(error);
			});
		});
	}
}